package cz.kelev.dashman.services.nlp

private val RU_WEEKDAYS = listOf(
    "понедельник","вторник","среда","четверг","пятница","суббота","воскресенье"
)
private val RU_WEEKDAYS_ACC = listOf(
    "понедельник","вторник","среду","четверг","пятницу","субботу","воскресенье"
)

object TaskTextExtractor {

    fun extractTaskText(raw: String): String {
        var t = (raw ?: "").trim().lowercase()

        // командные слова
        t = t.replace(Regex("\\b(пожалуйста|дашман|эй|слушай|короче|типа|ладно|окей|хорошо|блин|ну|давай|значит|вот|просто|кстати|короч|блять|бля|ладн)\\b"), " ")
        t = t.replace(Regex("\\b(напомни|напомнить|напоминай|запомни|запомнить|поставь\\s+напоминание|создай\\s+напоминание|добавь\\s+напоминание|мне|я|нужно|надо|должен|должна|должно|нужно\\s+будет|надо\\s+будет|не\\s+забудь|не\\s+забыть|поставь|создай|добавь|сделай|запиши|запишу|хочу|буду|планирую|собираюсь|планируется|нужна|нужен|надо\\s+будет)\\b"), " ")
        t = t.replace(Regex("^\\s*[,.-]*\\s*(что|чтобы)\\b\\s*"), " ")

        // диапазоны: "с 9 до 18", "с 9:00 до 18:00", "с 1 апреля по 30 апреля"
        val months = "январ|феврал|март|апрел|ма[йя]|июн|июл|август|сентябр|октябр|ноябр|декабр"
        t = t.replace(
            Regex("\\bс\\s+\\d{1,2}(?::\\d{2})?\\s+до\\s+\\d{1,2}(?::\\d{2})?\\b"), " "
        )
        t = t.replace(
            Regex("\\bс\\s+\\d{1,2}\\s+($months)\\w*\\s+по\\s+\\d{1,2}\\s+($months)\\w*\\b"), " "
        )
        t = t.replace(
            Regex("\\bначиная\\s+с\\s+\\d{1,2}(?::\\d{2})?\\b"), " "
        )
        // "с первого по", "с первое по", "с 1 по" — любые формы
        t = t.replace(
            Regex("\\bс\\s+(первого|второго|третьего|четвертого|пятого|шестого|седьмого|восьмого|девятого|десятого|одиннадцатого|двенадцатого|тринадцатого|четырнадцатого|пятнадцатого|шестнадцатого|семнадцатого|восемнадцатого|девятнадцатого|двадцатого|двадцать\\s+\\w+|тридцатого|тридцать\\s+первого|\\d{1,2})(?:\\s+($months)\\w*)?\\s+по(?:\\s+(\\d{1,2}|первого|второго|третьего|четвертого|пятого|шестого|седьмого|восьмого|девятого|десятого|\\w+)(?:\\s+($months)\\w*)?)?\\b"), " "
        )

        // "с утра до 10:00 вечера", "с вечера до ночи"
        t = t.replace(
            Regex("\\bс\\s+(утра|утром|вечера|вечером|ночи|ночью|дня|днём|днем)\\s+до\\s+\\d{1,2}(?::\\d{2})?(?:\\s+(утра|вечера|ночи|дня))?\\b"), " "
        )
        t = t.replace(
            Regex("\\bс\\s+\\d{1,2}(?::\\d{2})?\\s+до\\s+\\d{1,2}(?::\\d{2})?(?:\\s+(утра|вечера|ночи|дня))?\\b"), " "
        )

        // повторы
        t = t.replace(
            Regex("\\b(каждый\\s+день|каждое\\s+утро|каждый\\s+вечер|каждую\\s+неделю|ежедневно|по\\s+будням|по\\s+выходным|регулярно|каждый|каждую|каждое|каждые)\\b"),
            " "
        )

        // единицы повтора которые остаются после удаления "каждые"
        t = t.replace(
            Regex("\\b(\\d+\\s+)?(минут|минуту|минуты|час|часа|часов|дней|дня|день|недел[юи]|недель|месяц|месяца|месяцев)\\b"),
            " "
        )

        // время суток — всегда мусор
        t = t.replace(Regex("\\b(утром|утра|утро|днём|днем|дня|вечером|вечера|вечер|ночью|ночи|ночь|вчера)\\b"), " ")

        // сегодня/завтра/послезавтра — удаляем только ПЕРВОЕ вхождение (триггер времени)
        // второе вхождение = часть задачи, оставляем
        val dateTriggerRegex = Regex("\\b(сегодня|завтра|послезавтра)\\b")
        t = dateTriggerRegex.replaceFirst(t, " ")

        // явная дата: "26 марта", "5 апреля", "первого мая" и т.д.
        val explicitDateRegex = Regex("\\b\\d{1,2}\\s+($months)\\w*\\b")
        t = explicitDateRegex.replaceFirst(t, " ")

        val firstWeekday = (RU_WEEKDAYS + RU_WEEKDAYS_ACC)
            .mapNotNull { w -> Regex("\\b${Regex.escape(w)}\\b").find(t)?.range?.first?.let { w to it } }
            .minByOrNull { it.second }?.first
        if (firstWeekday != null) {
            // удаляем предлог в/на перед днём недели вместе с самим днём
            t = Regex("\\b(в|на)\\s+${Regex.escape(firstWeekday)}\\b").replaceFirst(t, " ")
                .let { result ->
                    // если предлога не было — удаляем просто день
                    if (result == t) Regex("\\b${Regex.escape(firstWeekday)}\\b").replaceFirst(t, " ")
                    else result
                }
        }

        // Сначала время с предлогом — удаляем первое целиком включая предлог
        val explicitTimeRegex =
            Regex("\\b(в|к|на)\\s*\\d{1,2}([:.]\\d{2})?(\\s*(час(а|ов)?|ч))?(\\s*(утра|дня|вечера|ночи))?\\b")
        t = explicitTimeRegex.replaceFirst(t, " ")

        // Голое время без предлога — удаляем только если перед ним НЕТ предлога в/к/на
        val bareTimeRegex = Regex("(?<![вкна]\\s)(?<![вкна])\\b\\d{1,2}:\\d{2}\\b")
        t = bareTimeRegex.replaceFirst(t, " ")

        // относительное: "через 2 часа", "через час"
        val relativeTimeRegex1 =
            Regex("\\bчерез\\s+\\d+\\s*(секунд(у|ы)?|минут(у|ы)?|час(а|ов)?|д(ень|ня|ней)|недел(ю|и|я)|месяц(а|ев)?|год(а|ов)?)\\b")
        val relativeTimeRegex2 =
            Regex("\\bчерез\\s+(секунд(у|ы)?|минут(у|ы)?|час(ик|ок)?|часа|день|дня|сутки|суток|недел(ю|и|я)|месяц|месяца|год|года)\\b")

        t = relativeTimeRegex1.replaceFirst(t, " ")
        t = relativeTimeRegex2.replaceFirst(t, " ")
        // голое "через" без единицы — остаток после предыдущей чистки
        t = t.replace(Regex("\\bчерез\\b"), " ")

        t = t.replace(Regex("^\\s*(и|а|ну|что|чтобы)\\b\\s*"), " ")

        // чистим
        t = t.replace(Regex("\\s+"), " ").trim().trim(',', '.', '-', ' ')
        return t
    }
}